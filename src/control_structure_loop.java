

public class control_structure_loop {


    public static void main(String [] args){

        //for loops
        for(int i =1; i<= 5; i++){
            System.out.printf("Hello from for loops \n");

        }
        //while loops
        int j = 1;
        while (j <= 5) {
            System.out.println("Hello from while loops");
            j++;
        }
        //do while loops
        int k = 1;
        do{
            System.out.println("Hello from do while");
            k++;
    }while (k<= 5);
}}
